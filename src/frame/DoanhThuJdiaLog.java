    private void loadDoanhThu() {
        String daystart = txtStartDay.getText();
        String dayend = txtEndDay.getText();
        if (daystart.equals("") || dayend.equals("")) {
            DialogHelper.alertError(this, "Không có dữ liệu");
            return;
        }
        modelTable = (DefaultTableModel) tbDoanhThu.getModel();
        modelTable.setRowCount(0);
        try {
        Date startday = (Date) DateHelper.toDate(daystart, "dd-MM-yyyy");
        Date endday = (Date) DateHelper.toDate(dayend, "dd-MM-yyyy");
            List<Object[]> list = dtdao.getRevenueByDay(startday, endday);
            for (Object[] ob : list) {
                modelTable.addRow(ob);
            }
        }
        catch (Exception e) {
            DialogHelper.alertError(this, "Không đúng không đúng định dạng dd-MM-yyyy!");
        }
    }

